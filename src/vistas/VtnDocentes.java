    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
           
        objDoc.setNombre(txtNombre.getText());
        objDoc.setEdad(Integer.parseInt(txtEdad.getText()));
        objDoc.setEmail(txtEmail.getText());
        objDoc.setCodigo(txtCodigo.getText());
        objDoc.setCantHoras(Integer.parseInt(txtHoras.getText()));
        objDoc.setPagoHora(Double.parseDouble(txtPago.getText()));
      
        JOptionPane.showMessageDialog(this, "Docente guardado correctamente");
        
        limpiarCajas();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String buscado = txtBuscar.getText();
        
        bd.abrirConexion();
        objAl = bd.consultarAlumno(buscado);
        bd.cerrarConexion();
        
        if(objAl != null) {
            JOptionPane.showMessageDialog(this, "Alumno encontrado, lo puedes evaluar");
            mostrarComponentes();
        }else{
           JOptionPane.showMessageDialog(this, "Alumno No existe"); 
           ocultarComponentes();
        }
        limpiarCajas();
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEvaluarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEvaluarActionPerformed
        
        objDoc.evaluarAlumno(objAl,
                Double.parseDouble(txtMate.getText()),
                Double.parseDouble(txtFisica.getText()),
                Double.parseDouble(txtProgra.getText()) );
        
        bd.abrirConexion();
        bd.actualizarAlumno(objAl);
        bd.cerrarConexion();
        
        JOptionPane.showMessageDialog(this, "Alumno Evaluado");
        ocultarComponentes();
        limpiarCajas();
    }//GEN-LAST:event_btnEvaluarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String eliminado = txtEliminar.getText();
        bd.abrirConexion();
        objAl = bd.consultarAlumno(eliminado);
        
        if(objAl != null){
            int opcion = JOptionPane.showConfirmDialog(this,
                    "¬øDeseas eliminar al alumno :" + objAl.getNombre());
            
            if(opcion == 0) {
                bd.eliminarAlumno(eliminado);
                JOptionPane.showMessageDialog(this,
                        "Alumno Eliminado de la BD");
            }else{
                JOptionPane.showMessageDialog(this,
                        "El Alumno sigue activo en la BD");
            }
        }else{
            JOptionPane.showMessageDialog(this,
                    "No existe el Alumno con ese c√≥digo en la BD");
        }
        bd.cerrarConexion();
        
    }//GEN-LAST:event_btnEliminarActionPerformed

